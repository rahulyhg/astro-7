package com.astro.horoscope.domain;

public class ProfileHeader {
	
	private String godImage;
	private String omText;
	private String chant;
	private String astrologerDesc;
	private String headerSeparator;
	
	public String getGodImage() {
		return godImage;
	}
	public void setGodImage(String godImage) {
		this.godImage = godImage;
	}
	public String getOmText() {
		return omText;
	}
	public void setOmText(String omText) {
		this.omText = omText;
	}
	public String getChant() {
		return chant;
	}
	public void setChant(String chant) {
		this.chant = chant;
	}
	public String getAstrologerDesc() {
		return astrologerDesc;
	}
	public void setAstrologerDesc(String astrologerDesc) {
		this.astrologerDesc = astrologerDesc;
	}
	public String getHeaderSeparator() {
		return headerSeparator;
	}
	public void setHeaderSeparator(String headerSeparator) {
		this.headerSeparator = headerSeparator;
	}
}
